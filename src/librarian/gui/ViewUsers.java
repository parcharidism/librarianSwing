    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int userID = 0;
        int row = jTableUsers.getSelectedRow();
        userID = Integer.parseInt(jTableUsers.getModel().getValueAt(row, 0).toString());
        int response = JOptionPane.showConfirmDialog(null, "If the user owns books they will be considered as returned", "Proceed with user deletion?",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        // If the reply is Yes proceed with return
        if (response == JOptionPane.YES_OPTION) {
            boolean books_returned = ReturnHandler.returnBookFromDeletedUser(userID);
            boolean updated = UsersHandler.deleteUser(userID);
            if (updated && books_returned) {
                JOptionPane.showMessageDialog(null, "The user with id " + userID + " has been deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
                showUsers(null);
            } else {
                JOptionPane.showMessageDialog(null, "Error While deleting user " + userID, "Error", JOptionPane.WARNING_MESSAGE);
            }

        }

    }//GEN-LAST:event_deleteBtnActionPerformed

