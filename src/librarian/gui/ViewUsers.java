    private void showUsers(ResultSet rs) {
        int role = 0;
        String roleStr = "", mailAddr = "";
        Date date = null;
        SimpleDateFormat formatter;

        if (rs == null) {
            rs = UsersHandler.selectUser(0, -1, null); // shows all users (check Oracle Procedure)
        }

        DefaultTableModel model = (DefaultTableModel) jTableUsers.getModel();
        model.setRowCount(0);
        try {
            while (rs.next()) {

                role = rs.getInt(4);
                switch (role) {
                    case 1:
                        roleStr = "Admin";
                        break;
                    case 2:
                        roleStr = "User";
                        break;
                    case 3:
                        roleStr = "Client";
                        break;
                }

                date = rs.getDate(6);
                formatter = new SimpleDateFormat("dd/MM/yyyy");
                String strDate = formatter.format(date);

                String address8 = "";
                if (rs.getString(7).length() > 8) {
                    address8 = rs.getString(7).substring(0, 8);
                } else {
                    address8 = rs.getString(7);
                }

                String active = rs.getInt(8) == 1 ? "Enabled" : "Disabled";
                Object data[] = {rs.getInt(1), rs.getString(2), rs.getString(3), roleStr, rs.getString(5), strDate, address8, active};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewUsers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
