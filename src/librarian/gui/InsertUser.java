    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        boolean inserted = UsersHandler.insertUser(nameTxt.getText(), surnameTxt.getText(),
                roleCombo.getSelectedIndex(), usernameTxt.getText(),
                passwordTxt.getText(), mailTxt.getText(), notesTxt.getText());

        if (inserted) {
            JOptionPane.showMessageDialog(null, "User Inserted successfully", "Create New User", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Error While Inserting!", "Create New User", JOptionPane.WARNING_MESSAGE);
            this.setVisible(false);
        }
        MenuAdmin menuAdmin = new MenuAdmin();
        menuAdmin.setVisible(true);
    }//GEN-LAST:event_insertBtnActionPerformed

