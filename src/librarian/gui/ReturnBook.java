    private void selectUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectUserBtnActionPerformed
        // Populate the books
        int userID = Integer.parseInt(userIds.get(customerCombo.getSelectedIndex()).toString());
        ResultSet rsB = BookHandler.selectBooksByUser(userID); // show all available books
        DefaultTableModel model = (DefaultTableModel) jTableBooks.getModel();

        model.setRowCount(0);
        SimpleDateFormat formatter;
        formatter = new SimpleDateFormat("dd/MM/yyyy");
        String date;
        try {
            while (rsB.next()) {
                date = formatter.format(rsB.getDate(5));
                Object data[] = {rsB.getString(1), rsB.getString(2), rsB.getString(3), rsB.getString(4), date};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_selectUserBtnActionPerformed

