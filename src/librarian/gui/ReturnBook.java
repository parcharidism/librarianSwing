    private void previewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previewBtnActionPerformed

        int bookID = 0, userID = 0;
        int row = jTableBooks.getSelectedRow();
        bookID = Integer.parseInt(jTableBooks.getModel().getValueAt(row, 0).toString());
        String date = jTableBooks.getModel().getValueAt(row, 4).toString();

        int customerComboIndex = customerCombo.getSelectedIndex();
        userID = Integer.parseInt(userIds.get(customerComboIndex).toString());

        int price = ReturnHandler.calculatePrice(date, bookID);
        int response = JOptionPane.showConfirmDialog(null, "Return value is " + price + " \u20ac", "Proceed with return?",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        // If the reply is Yes proceed with return
        if (response == JOptionPane.YES_OPTION) {
            boolean updated = ReturnHandler.returnBook(userID, bookID);
            if (updated) {
                JOptionPane.showMessageDialog(null, "The Book has been returned", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                MenuAdmin menuAdmin = new MenuAdmin();
                menuAdmin.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error While returning the book!", "Error", JOptionPane.WARNING_MESSAGE);
                //this.setVisible(false);
            }

        }

    }//GEN-LAST:event_previewBtnActionPerformed

