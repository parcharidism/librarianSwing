    public ViewUser(int userID) {
        initComponents();
        this.userID = userID;
        ResultSet rs = null;
        int role = 0, active = -1;
        rs = UsersHandler.selectUser(userID,-1, null);

        try {
            rs.next();

            nameTxt.setText(rs.getString(1));
            surnameTxt.setText(rs.getString(2));
            role = rs.getInt(3);
            usernameTxt.setText(rs.getString(4));
            regDayTxt.setText(UsersHandler.dateToString(rs.getDate(5)));
            mailTxt.setText(rs.getString(6));
            active = rs.getInt(7);
            notesTxt.setText(rs.getString(8));
        } catch (SQLException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Roles Resultset
        rs = RoleHandler.selectRole();
        //roleCombo.addItem("- Select Role - ");
        try {
            while (rs.next()) {
                roleCombo.addItem(rs.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        }

        roleCombo.setSelectedIndex(role - 1);
        activeCombo.addItem("Disabled");
        activeCombo.addItem("Active");
        activeCombo.setSelectedIndex(active);

    }

